public class BooleanUnicode {

	public static void main(String[] args) {
		boolean var = false;
		System.out.println(var);

		System.out.println("Unicode :");
		System.out.println('\u002F');
		System.out.println('\u003F');
		System.out.println('\u0031');
		System.out.println('\u0024');
		System.out.println("__________________________________");
		int a=15;
		System.out.format(a+" in Hexa is = %x",a);
		System.out.println();
		int b=92;
		System.out.format(b+" in Hexa is = %x",b);
		System.out.println();
		int c=86;
		System.out.format(c+" in Hexa is = %o",c);

	}

}
