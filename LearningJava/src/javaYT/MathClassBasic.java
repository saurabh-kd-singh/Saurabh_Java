package javaYT;

public class MathClassBasic {

	public static void main(String[] args) {
		
		int n = 16;
		double d = 6.35;
		
		System.out.println(Math.abs(d)); //Gives absolute value.
		System.out.println(Math.pow(n,3));  //Power Operator
		System.out.println(Math.sqrt(n));  //Square Root Function
		System.out.println(Math.min(6,5));
		System.out.println(Math.round(d));  //Round off
		System.out.println(Math.random());  // generates a random number  >= 0.0  and < 1.0

	}

}
